package Loops;

public class ForLoop {


    public static void main(String[] args) {

        // For loop is used when we know how many times to iterate
        // Eg: statement1 = line of code in first run
        //     statement2 = condition checker (true/false
        //     statement3 = line of code for the end of every ITERATION

        // for (statement1, statement2, statement3....) {
            // Run this block of code
        //  }

        for (int i = 0; i < 10; i++) {
            System.out.println(i);
        }
        System.out.println("Iteration ended");


    }
}
